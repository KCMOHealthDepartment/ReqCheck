  def find_all_vax_codes_by_cvx(cvx_code)
    return_vax_codes = []
    VAXCODES.each do |key, value|
      if value.map { |element| element[3] }.include?(cvx_code)
        return_vax_codes << key
      end
    end
    raise "No Vaccine Codes Found for cvx #{cvx_code}" if return_vax_codes == []
    return_vax_codes
  end
